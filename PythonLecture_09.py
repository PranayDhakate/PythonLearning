print("--------Break-------")
for i in range(10):
    if i == 6:
        break #It breaks the loop
    print(i)
print("--------Pass-------")
for i in range(10):
    if i == 6:
        pass #It does nothing literally nothing
    print(i)
print("--------Continue-------")
for i in range(10):
    if i == 6:
        continue #It skips the value and move forward.
    print(i)